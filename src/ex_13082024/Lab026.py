#Task-2 Finding Max, Addition, Subtraction, Multiplication and Division of user input numbers

a = float(input("Enter first number: "))
b = float(input("Enter second number: "))
max_number = max(a, b)
sum = sum([a, b])
sub = (a-b)
multiply = (a * b)
div = (a / b)
print("The maximum number is: ", max_number)
print("The Sum of the numbers is: ", sum)
print("The Subtraction of the numbers is: ", sub)
print("The Multiplication of the numbers is: ", multiply)
print("The Division of the numbers is: ", div)


